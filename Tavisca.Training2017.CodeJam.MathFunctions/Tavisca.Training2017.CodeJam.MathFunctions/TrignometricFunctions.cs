using System;
using System.Collections.Generic;
using System.Linq;
using System.Text;
using System.Threading.Tasks;

namespace Tavisca.Training2017.CodeJam.MathFunctions
{
    class TrignometricFunctions
    {
        internal double FindCosAngle(double number)
        {
            double result = Math.Cos(number);
            return result;
        }

        internal double FindSinAngle(double number)
        {
            double result = Math.Sin(number);
            return result;
        }

        internal double FindTanAngle(double number)
        {
            double result = Math.Tan(number);
            return result;
        }
    }
}
